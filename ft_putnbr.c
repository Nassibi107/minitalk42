#include "minitalk.h"

void	ft_putnub(int n)
{
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n *= -1;
		ft_write('_');
	}
	if (n < 10)
		ft_write(n + 48);
	else
	{
		ft_putnub(n / 10);
		ft_putnub(n % 10);
	}
}
